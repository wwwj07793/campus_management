from __future__ import annotations

from typing import Any, Protocol


class StudentRepositoryProtocol(Protocol):
    def create(self, **fields: Any) -> Any: ...
    def get_by_id(self, id: Any) -> Any | None: ...
    def get_by_student_id(self, student_id: str) -> Any | None: ...
    def list_all(self) -> list[Any]: ...
    def search(self, **filters: Any) -> list[Any]: ...
    def update(self, lookup_student_id: str, **fields: Any) -> Any | None: ...
    def set_gpa(self, student_id: str, gpa: float) -> Any | None: ...
    def delete(self, student_id: str) -> bool: ...


class CourseRepositoryProtocol(Protocol):
    def create(self, **fields: Any) -> Any: ...
    def get_by_id(self, id: Any) -> Any | None: ...
    def get_by_identity(self, course_code: str, teacher: str, schedule: str) -> Any | None: ...
    def list_by_code(self, course_code: str) -> list[Any]: ...
    def list_by_teacher(self, teacher: str) -> list[Any]: ...
    def list_all(self) -> list[Any]: ...
    def update(self, id: Any, **fields: Any) -> Any | None: ...
    def delete(self, id: Any) -> bool: ...


class EnrollmentRepositoryProtocol(Protocol):
    def create(self, student_id: Any, course_id: Any, update_course_count: bool = True) -> Any: ...
    def get(self, student_id: Any, course_id: Any) -> Any | None: ...
    def exists(self, student_id: Any, course_id: Any) -> bool: ...
    def list_by_student_id(self, student_id: Any) -> list[Any]: ...
    def list_by_student_number(self, student_number: str) -> list[Any]: ...
    def list_by_course_id(self, course_id: Any) -> list[Any]: ...
    def delete(self, student_id: Any, course_id: Any, update_course_count: bool = True) -> bool: ...


class GradeRepositoryProtocol(Protocol):
    def create(self, student_id: Any, course_id: Any, score: float) -> Any: ...
    def get(self, student_id: Any, course_id: Any) -> Any | None: ...
    def upsert(self, student_id: Any, course_id: Any, score: float) -> Any: ...
    def list_by_student_id(self, student_id: Any) -> list[Any]: ...
    def list_by_student_number(self, student_number: str) -> list[Any]: ...
    def list_by_course_id(self, course_id: Any) -> list[Any]: ...
    def delete(self, student_id: Any, course_id: Any) -> bool: ...
